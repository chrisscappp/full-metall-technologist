// Для расчета припуска на износ в вырбуке [zmin, zmax]
export const workingToolAllowanceCoeffs = (thin: number) => {
	if (thin >= 0 && thin < 0.3) return [0, 0]
	if (thin >= 0.3 && thin < 0.5) return [0.02, 0.04]
	if (thin >= 0.5 && thin < 0.8) return [0.03, 0.06]
	if (thin >= 0.8 && thin < 1.0) return [0.05, 0.08]
	if (thin >= 1.0 && thin < 1.2) return [0.06, 0.10]
	if (thin >= 1.2 && thin < 1.5) return [0.08, 0.12]
	if (thin >= 1.5 && thin < 1.8) return [0.10, 0.16]
	if (thin >= 1.8 && thin < 2.0) return [0.12, 0.20]
	if (thin >= 2.0 && thin < 2.2) return [0.14, 0.22]
	if (thin >= 2.2 && thin < 2.5) return [0.17, 0.25]
	if (thin >= 2.5 && thin < 2.8) return [0.20, 0.28]
	if (thin >= 2.8 && thin < 3.0) return [0.22, 0.32]
	if (thin >= 3.0 && thin < 3.5) return [0.24, 0.36]
	if (thin >= 3.5 && thin < 4.0) return [0.32, 0.46]
	if (thin >= 4.0 && thin < 4.5) return [0.40, 0.56]
	if (thin >= 4.5 && thin < 5.0) return [0.50, 0.68]
	if (thin >= 5.0 && thin < 6.0) return [0.60, 0.80]
	if (thin >= 6 && thin < 7) return [0.8, 1.0]
	if (thin >= 7 && thin < 8) return [1.0, 1.3]
	if (thin >= 8 && thin < 9) return [1.3, 1.6]
	if (thin >= 9 && thin < 10) return [1.5, 1.8]
	if (thin >= 10 && thin < 11) return [1.8, 2.2]
	if (thin >= 11 && thin < 12) return [2.1, 2.5]
	if (thin >= 12 && thin < 13) return [2.4, 2.7]
	if (thin >= 13 && thin < 14) return [2.7, 3.0]
	if (thin >= 14 && thin < 15) return [3.0, 3.4]
	if (thin >= 15 && thin < 16) return [3.3, 3.7]
	if (thin >= 16 && thin < 17) return [3.6, 4.0]
	if (thin >= 17 && thin < 18) return [3.9, 4.4]
	if (thin >= 18 && thin < 19) return [4.2, 4.7]
	if (thin >= 19 && thin < 20) return [4.5, 4.0]
	if (thin >= 20) return [4.9, 5.4]
	return [0, 0]
}
